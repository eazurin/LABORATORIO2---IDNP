package com.example.loginsample;

public class AccountEntity {
    private String firstnane;

    private String lastnane;

    private String email;

    private String phone;

    private String usernane;

    private String password;

    public String getFirstnane() {
        return firstnane;
    }

    public void setFirstnane(String firstnane) {
        this.firstnane = firstnane;
    }

    public String getLastnane() {
        return lastnane;
    }

    public void setLastnane(String lastnane) {
        this.lastnane = lastnane;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUsernane() {
        return usernane;
    }

    public void setUsernane(String usernane) {
        this.usernane = usernane;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
